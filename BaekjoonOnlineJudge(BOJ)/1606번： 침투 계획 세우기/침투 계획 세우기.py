import sys
reading = sys.stdin.readlines()
x,y = map(int, reading[0].strip().split(" "))

if y==0:
    n=1+sum(range(0, (6*x)+1, 6))
else:
    n=1+sum(range(0, (6*(x+y-1))+1, 6))+y
print(n)